package gov.healthit.chpl.domain;

public class SurveillanceNonconformityStatus {
	private Long id;
	private String name;
	
	public SurveillanceNonconformityStatus() {}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
